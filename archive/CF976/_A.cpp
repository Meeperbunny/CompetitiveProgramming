void TC() {
    ll n, k;
    cin >> n >> k;
    if (k == 1) {
        cout << n << endl;
        return;
    }
    ll c = 1;
    while(c * k <= n) {
        c *= k;
    }
    ll ops = 0;
    while(c >= 1) {
        ops += n / c;
        n %= c;
        c /= k;
    }
    cout << ops << endl;
}

