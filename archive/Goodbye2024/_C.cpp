void TC() {
    ll n, k;
    cin >> n >> k;
    ll cnt = 0;
    ll cc = 1;
    ll segsize = n;
    while(segsize >= k) {
        if (segsize & 1) {
            cnt += cc;
            segsize = (segsize - 1) / 2;
        }
        else {
            segsize = segsize / 2;
        }
        cc *= 2;
    }
    cout << (cnt * (n + 1)) / 2 << endl;
}

