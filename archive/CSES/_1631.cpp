void TC() {
    int n; cin >> n;
    vector<ll> nums(n);
    for(int i = 0; i < n; i++) cin >> nums[i];
    ll ma = 0;
    ll s = 0;
    for(auto e : nums) ma = max(ma, e), s += e;
    if (ma > s / 2) {
        cout << ma * 2 << endl;
    }
    else {
        cout << s<< endl;
    }
}

