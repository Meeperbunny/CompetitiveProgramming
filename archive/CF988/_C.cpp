void TC() {
    int n; cin >> n;
    if (n <= 4) cout << -1 << endl;
    else {
        for(int i = 2 * int(n / 2); i >= 6; i -= 2) {
            cout << i << ' ';
        }
        cout << "2 4 5 3 1" << ' ';
        for(int i = 7; i <= n; i += 2) cout << i << ' ';
        cout << endl;
    }
}

